    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        try{
            Funcion f = new Funcion(txtFuncion.getText());
            String funcionDerivada = txtFuncion.getText();
            Derivadas derivada = new Derivadas();
            derivada.setFuncion(funcionDerivada);
            derivada.derivarX();
            
            //System.out.println(derivada.getFuncion());
            Funcion fd = new Funcion(derivada.getFuncion());

            Newton n = new Newton();
            String raiz = n.raiz(f,Double.parseDouble(txtXi.getText()),Double.parseDouble(txtError.getText()),fd);
            txtAreaResultado.setText(n.getIteraciones());
            
            btnLimpiar.setEnabled(true);
            btnCalcular.setEnabled(false);
            txtFuncion.setEditable(false);
            txtXi.setEditable(false);
            txtError.setEditable(false);
            
            JOptionPane.showMessageDialog(null,"Valor de La Raiz : "+raiz,"Valor",JOptionPane.INFORMATION_MESSAGE);
        }catch(Exception e){
            frmNewton objInicio = new frmNewton();
            objInicio.setVisible(true);
            this.dispose();
            JOptionPane.showMessageDialog(null,"Error en los calculos","ERROR",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

